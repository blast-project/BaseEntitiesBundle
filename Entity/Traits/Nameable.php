namespace Blast\Bundle\BaseEntitiesBundle\Entity\Traits;

trait Nameable
{
    /**
     * @var string
     */
    protected $name = '';

    /**
     * @param string $name
     *
     * @return Addressable
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}
